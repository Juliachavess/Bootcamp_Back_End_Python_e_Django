'''Faça um Programa que peça dois números, realize as principais
operações soma, subtração, multiplicação, divisão'''

num01 = int(input('Digite o primeiro número: '))
num02 = int(input('Digite o segundo número: '))

soma = num01+num02
print(F'O valor da soma é {soma}')

sub = num01-num02
print(F'O valor da subtração é {sub}')

mult = num01*num02
print(F'O valor da multiplicação é {mult}')

divi = num01/num02
print(F'O valor da divisão é {divi}')
